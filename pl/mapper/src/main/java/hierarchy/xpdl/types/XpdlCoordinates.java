package hierarchy.xpdl.types;

public class XpdlCoordinates {

    private String x;
    private String y;

    public XpdlCoordinates(String x, String y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }
}
